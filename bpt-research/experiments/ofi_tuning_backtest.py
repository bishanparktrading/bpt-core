"""Backtest AS baseline (ofi_weight_bps=0) vs tuned (recommended weight)
across 6 HL instruments, replaying 10 canon days.

For each instrument:
  - generate baseline + tuned config from a template (XMR backtest is the
    seed) with instrument-specific qty / max_inventory sized to ~$80
    notional per order, ~$800 max inventory
  - run bpt-backtester twice (--canon = all 10 days at once for 10× sample
    leverage in a single run)
  - record both summary.json paths

Outputs:
  /tmp/bt_runs/<instrument>_<baseline|tuned>/summary.json
  /tmp/bt_runs/comparison.csv — joined PnL / sharpe deltas
"""

from __future__ import annotations
import json
import shutil
import subprocess
import sys
from pathlib import Path

import pandas as pd

REPO = Path('/home/jseow/code/bpt-core')
BACKTESTER = REPO / 'bazel-bin/bpt-backtester/bpt-backtester'
MAPPING = REPO / 'config/instruments/instrument_mapping.hyperliquid-mainnet.json'
TEMPLATE = REPO / 'bpt-strategy/config/strategies/avellaneda_stoikov.backtest-xmr.toml'
PROFILE = REPO / 'deploy/config/profile/backtest-hl.toml'
AERON = REPO / 'deploy/config/aeron/streams.toml'
CANON_DIR = Path('/tmp/bpt_canon')
OUT_BASE = Path('/tmp/bt_runs')

INSTANCE_TEMPLATE = """# Auto-generated instance config for OFI tuning backtest.
profile_config = "{profile}"
backtest_mode  = true
strategy_config = "{strategy}"
aeron_config = "{aeron}"

[aeron]
media_driver_dir = "/dev/shm/aeron-bpt"

[strategy]
correlation_id = 1006

[metrics]
port = 9104
"""

# Instruments to backtest. order_qty sized so ~$80 notional/order;
# max_inventory ~$800. ofi_weight_bps from tuning_candidates CSV (already
# 0.5× headroom applied). XMR uses its unstable IC mean × 0.5 as a
# deliberately-tuned value to test the "don't enable" hypothesis.
INSTRUMENTS = [
    # (symbol, hl_pair, median_price, ofi_weight_tuned)
    ('XMR',  'XMR/USD:PERPETUAL',  385.25,  0.049),   # unstable in panel — expect no edge
    ('APE',  'APE/USD:PERPETUAL',    0.145, 0.107),
    ('ZEC',  'ZEC/USD:PERPETUAL',  659.26,  0.046),
    ('SOL',  'SOL/USD:PERPETUAL',   86.92,  0.088),
    ('ETH',  'ETH/USD:PERPETUAL', 2130.85,  0.073),
    ('AAVE', 'AAVE/USD:PERPETUAL',  87.89,  0.116),
]


def render_config(symbol: str, pair: str, price: float, ofi_weight_bps: float,
                  out_path: Path) -> None:
    """Render strategy config from XMR template, override instrument + size + ofi."""
    src = TEMPLATE.read_text()

    # Replace instrument
    src = src.replace('"XMR/USD:PERPETUAL"', f'"{pair}"')

    # Size order_qty so ~$80 notional/order; max_inventory ~$800.
    qty = max(0.001, round(80.0 / price, 4))
    max_inv = max(0.01, round(800.0 / price, 3))
    src = src.replace('order_qty = 0.2', f'order_qty = {qty}')
    src = src.replace('order_qty_min           = 0.2', f'order_qty_min           = {qty}')
    src = src.replace('max_inventory = 2.0', f'max_inventory = {max_inv}')

    # Inject ofi_weight_bps + window + L2-top-1 OFI BEFORE the
    # [strategy.params.fair_value] subsection. Appending at EOF would put
    # these inside that subsection (TOML nesting), making the keys invisible
    # at params.* level.
    ofi_block = f"""
# Auto-generated by ofi_tuning_backtest.py.
# order_book_depth=1: subscribes to L2 top-of-book so st.book.ready() goes
# true and the OFI gate activates. Sets OFICalculator max_levels=1, which
# matches the panel calibration that produced these weights.
order_book_depth = 1
ofi_window_ms    = 1000.0
ofi_weight_bps   = {ofi_weight_bps}

"""
    marker = '[strategy.params.fair_value]'
    if marker in src:
        src = src.replace(marker, ofi_block + marker, 1)
    else:
        src += ofi_block
    out_path.write_text(src)


def render_instance(strategy_path: Path, instance_path: Path) -> None:
    instance_path.write_text(INSTANCE_TEMPLATE.format(
        profile=PROFILE, strategy=strategy_path, aeron=AERON))


def run_backtest(instance_path: Path, run_name: str) -> Path:
    """Run bpt-backtester, return path to results dir."""
    out_dir = OUT_BASE / run_name
    if out_dir.exists():
        shutil.rmtree(out_dir)
    out_dir.mkdir(parents=True)
    canons = sorted(CANON_DIR.glob('hl-2026-05-*-h00.canon'))
    cmd = [
        str(BACKTESTER),
        '--strategy-config', str(instance_path),
        '--instrument-mapping', str(MAPPING),
        '--canon', *[str(c) for c in canons],
        '--output-dir', str(out_dir),
        '--starting-capital', '1000',
        '--strategy-name', 'AvellanedaStoikov',
    ]
    print(f'  [{run_name}] running...', flush=True)
    log = out_dir / 'backtest.log'
    with open(log, 'w') as f:
        rc = subprocess.run(cmd, stdout=f, stderr=subprocess.STDOUT).returncode
    print(f'  [{run_name}] done rc={rc}', flush=True)
    return out_dir


def find_summary(run_dir: Path) -> dict | None:
    # bpt-backtester writes results/<run_id>/summary.json
    summaries = list(run_dir.rglob('summary.json'))
    if not summaries:
        return None
    with open(summaries[0]) as f:
        return json.load(f)


def main() -> int:
    OUT_BASE.mkdir(parents=True, exist_ok=True)
    cfg_dir = OUT_BASE / 'configs'
    cfg_dir.mkdir(exist_ok=True)

    rows = []
    for sym, pair, price, w_tuned in INSTRUMENTS:
        for variant, w in [('baseline', 0.0), ('tuned', w_tuned)]:
            strat_path = cfg_dir / f'strategy.as.{sym}.{variant}.toml'
            inst_path = cfg_dir / f'as.{sym}.{variant}.toml'
            render_config(sym, pair, price, w, strat_path)
            render_instance(strat_path, inst_path)
            run_name = f'{sym}_{variant}'
            run_dir = run_backtest(inst_path, run_name)
            summary = find_summary(run_dir)
            if summary is None:
                print(f'  [{run_name}] no summary.json (probably failed; see log)', flush=True)
                rows.append({'symbol': sym, 'variant': variant, 'weight': w,
                             'status': 'failed'})
                continue
            rows.append({
                'symbol': sym,
                'variant': variant,
                'weight': w,
                'total_pnl': summary.get('total_pnl'),
                'return_pct': summary.get('return_pct'),
                'sharpe_per_fill': summary.get('sharpe_per_fill'),
                'total_fills': summary.get('total_fills'),
                'win_rate_pct': summary.get('win_rate_pct'),
                'fees_paid_usd': summary.get('fees_paid_usd'),
                'max_drawdown_pct': summary.get('max_drawdown_pct'),
                'status': 'ok',
            })

    df = pd.DataFrame(rows)
    df.to_csv(OUT_BASE / 'comparison.csv', index=False)
    print('\n=== Per-instrument backtest summary ===')
    print(df.to_string(index=False))

    # Compute deltas (tuned − baseline) per instrument
    pivot = df[df.status == 'ok'].pivot(index='symbol', columns='variant',
                                         values=['total_pnl', 'sharpe_per_fill',
                                                 'total_fills', 'fees_paid_usd'])
    if not pivot.empty:
        delta = pd.DataFrame({
            'pnl_baseline':  pivot['total_pnl']['baseline'],
            'pnl_tuned':     pivot['total_pnl']['tuned'],
            'pnl_delta':     pivot['total_pnl']['tuned'] - pivot['total_pnl']['baseline'],
            'sharpe_base':   pivot['sharpe_per_fill']['baseline'],
            'sharpe_tuned':  pivot['sharpe_per_fill']['tuned'],
            'fills_base':    pivot['total_fills']['baseline'],
            'fills_tuned':   pivot['total_fills']['tuned'],
        })
        print('\n=== Delta (tuned − baseline) ===')
        print(delta.to_string())
        delta.to_csv(OUT_BASE / 'delta.csv')
    print(f'\nwrote {OUT_BASE}/comparison.csv and delta.csv')
    return 0


if __name__ == '__main__':
    sys.exit(main())
